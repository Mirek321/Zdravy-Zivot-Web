  <h4 class="nadpis">7. Čítanie nás robí empatickejšími</h4>
  <p>Áno, aj to vedci dokázali – vďaka čítaniu kníh sme empatickejší, vieme sa lepšie do druhých vcítiť, pochopiť ich. Obohacujeme tak emocionálnu sféru našej psychiky.
    Dôležité je aj to, že vďaka čítaniu kníh dokážeme lepšie rozumieť svojim vlastným emóciám,  dokážeme ich lepšie komunikovať a  kultivovaným spôsobom vyjadrovať. Čítanie kníh tak podporuje rozvoj emočnej inteligencie a empatie.</p>
  <h4 class="nadpis">8. Čítanie prináša radosť zo života</h4>
  <p>To, prečo väčšina ľudí číta, je najmä zábava, potešenie z čítania, zmysluplný a užitočný spôsob trávenia voľného času. Relaxácia.
    Čítanie – podobne ako hra – nám umožňuje únik od všedných starostí do sveta literárnej fikcie, chráni nás pred rutinou každodenných starostí, pred vyčerpaním a vyhorením.
    
     </p>
  <h4 class="nadpis">9. Čítanie nám dodáva sebavedomie</h4>
  <img class="citanie" src="img/knizka4.jpg" height="300" align="right"> 
  <p>Poviete si – ako mi môžu knihy zvyšovať sebavedomie?
    „Nuž, chápanie seba samého, porozumenie iným spolu s ďalšími hodnotami plynúcimi z čítania v oblasti sociálnych vzťahov, spoločenského uplatnenia či  kultúrneho vyžitia  prispieva k formovaniu vyrovnanej osobnosti s uspokojivým sebahodnotením a zdravým sebavedomím. To všetko sú charakteristiky osobnosti, ktoré z nás robia lepších ľudí,“ vysvetľuje profesorka Zápotočná z SAV.
    Pri čítaní totiž často sledujeme, ako postavy riešia určité problémy, ako sa vyrovnávajú so starosťami a trápením. Aj podvedome sa tak učíme do budúcnosti, keď budeme stáť pred problémom a zrazu intuitívne prídeme na to, ako konať. Zostaneme pokojnejší, nad vecou a pragmatickejší.</p>
  <br><br>
    <h4 class="nadpis">10. Čítanie pomáha prosperite celej spoločnosti</h4>
  <p>Profesorka Oľga Zápotočná napokon dodáva aj tento pozitívny vplyv na náš život – úroveň vzdelania a čitateľskej gramotnosti obyvateľstva je veľmi silným nástrojom kontroly vládnucej moci. Ako taká ide ruka v ruke s úrovňou  demokracie a ekonomickej prosperity spoločnosti.
    Na druhej strane negramotnosť, resp. nízka úroveň vzdelanosti obyvateľstva udržuje pri živote totalitné autokratické režimy, spojené s chudobou, vysokou nezamestnanosťou a kriminalitou. „História potvrdzuje, že vzdelania a gramotnosti širokých más obyvateľstva sa mocní vládcovia vždy obávali.  Pálenie kníh je toho najlepším príkladom,“ dodáva odborníčka z Ústavu výskumu sociálnej komunikácie SAV.</p>
  
