  <h4 id="scrollspyHeading1"></h4>
  <p>Niekto vezme knihu do ruky raz za rok, iný bez knihy nevydrží ani týždeň. Sú dokonca ľudia, ktorí musia čítať
    stále, pretože pociťujú „čitateľský deficit“ – jednoducho každý deň musia prečítať aspoň pár stránok. Odborníci
    potvrdzujú, že hodnota čítania je nevyčísliteľná. „Čítanie kníh nám nesmierne obohacuje a zlepšuje život, pretože
    doslova prežívame tisíce príbehov. Keď čítame knihu, vlastne máme príležitosť akoby komunikovať s tými
    najvzdelanejšími a najmúdrejšími ľuďmi. A to nenahradí žiadny internet, ani iné technológie,“ tvrdí pre LepšíDeň.sk
    profesorka prof. Oľga Zápotočná z Ústavu výskumu sociálnej komunikácie SAV.</p>
  <p>Čítaním a jeho vplyvmi na naše zdravie a psychiku sa zaoberá už dlhé roky. Čerpá z psychológie a analyzuje výsledky
    rôznych výskumov, ktoré dokumentujú, že čítanie prospieva a je zdrojom vývinu prakticky všetkých psychických funkcií
    človeka.</p>
  <p>Pozrime sa spoločne na 10 pozitívnych vplyvov čítania kníh na naše zdravie, psychiku, na naše sebavedomie, či
    emocionálny vývin.</p>
    <img src="img/knizka2.jpg" class="obrazok2">
  <h4 class="nadpis">1. Čítanie prospieva zdraviu</h4>
  <p>
    Keď to vezmeme z nadhľadu a s úsmevom – náruživé čítanie kníh zdraviu neškodí. Na rozdiel od iných návykov a
    závislostí, ako sú alkohol, fajčenie, drogy ap.
    A nejde len psychické zdravie, ktoré je spojené s pozitívnymi emóciami, relaxáciou, redukciou stresu či zdravým
    sebavedomím. Ide aj o zdravie fyzické.
    Samostatnú kapitolu v tomto smere predstavujú prínosy čítania vo vzťahu k fungovaniu a kondícii mozgových funkcií.
    Čítanie kníh sa najmä u starších ľudí osvedčuje ako spôsob tréningu pamäte, koncentrácie pozornosti, prevencie
    starnutia a ďalších involučných zmien. Ako svojho času povedal Joseph Addison, „čítanie je pre myseľ tým, čo
    cvičenie pre telo.“
  </p>
  <h4 class="nadpis">2. Čítanie predlžuje život</h4>
  <p>
  Ak je pravda, že čítanie prospieva psychickému aj fyzickému zdraviu, potom je možné, že čítanie skutočne predlžuje život. To vraví logika.
A keby to aj neplatilo objektívne, určite to sedí subjektívne. Spomeňme si na citát Georgea R.R.Martina, autora slávnej Hry o tróny:</p>
    <p>
      „Človek, ktorý číta prežije tisíc životov, kým zomrie. Človek, ktorý nikdy nečíta, prežije len jeden.“
    </p>
    <p>Pri čítaní prežívame tisíce príbehov, cestujeme časom, bojujeme proti zlu, zažívame lásku, radosť… Prežívame toho oveľa viac ako v bežnom živote, takže nám to vlastne predlžuje a obohacuje náš vlastný život.</p>
  <h4 class="nadpis">3. Čítanie kultivuje našu reč</h4>
  <p>Čítaním kníh sa učíme jazyk lepšie používať – rozprávať, vyjadrovať sa  a komunikovať.  Čítanie v každom veku podporuje rozvoj slovnej zásoby, súvislého vyjadrovania sa a mnohých ďalších jazykových kompetencií.
    Vďaka čítaniu jazyk aj lepšie spoznávame. Učíme sa „o jazyku“, jeho bohatosti, rôznorodosti a úžasných možnostiach vyjadrovania sa.</p>
    <p>Odborníci odporúčajú čítanie kníh najmä v období osvojovania si jazyka vo veku od 0 do 6 rokov. V tomto období výrazne prispieva k rozvoju slovnej zásoby. Treba deťom čítať odmalička, už od narodenia a vyberať jednoduchšie texty. Aj keď máte pocit, že to dieťa nevníma, prirodzene nerozumie, v podvedomí sa mu to ukladá.
      Neskôr treba deťom čítať a diskutovať s nimi o prečítanom – či už sú na prvom stupni, druhom stupni základnej školy, ale pokojne aj na strednej škole.
      Pozitívne účinky čítania deťom totiž pretrvávajú po celý život.</p>
  <img src="img/knizka6.jpg" class="obrazok2">
      <h4 class="nadpis">4. Vďaka čítaniu spoznávame svet</h4>
  <p>Svet samozrejme poznávame aj priamo našimi zmyslami: zrakom, sluchom ap. Najdôležitejším zdrojom poznania je však jeho sprostredkovaná podoba, ktorá sa k nám dostáva v jazyku. Neuveriteľne bohatým zdrojom poznania, čiže nadobúdania vedomostí a vzdelania, je jazyk knihy.</p>
 <p>Niektorí budú oponovať, že internet v súčasnosti poskytuje neobmedzené množstvo informácií a z tohto hľadiska je aj podľa profesorky Zápotočnej bezkonkurenčné. „Špecifickou devízou knihy je však najmä spôsob spracovania informácií autorom. Čítaním kníh máme možnosť sledovať, ako autor o tom, čo píše premýšľa, hľadá súvislosti, vyvodzuje závery – ilustruje svoj vlastný spôsob premýšľania, poznávania a objavovania sveta,“ vysvetľuje pre portál LepšíDeň.sk. A to všetko zanecháva v našej mysli nezmazateľné stopy.</p>
  <h4 class="nadpis">5. Čítanie podporuje rozvoj poznávacích funkcií a myslenia </h4>
  <p>Toto je nesmierne dôležité najmä u detí. Pravidelné čítanie má blahodarný účinok na všetky poznávacie psychické funkcie človeka, a to nielen na ich vývin v ranom detstve, ale počas celého života.
    Čítanie si vyžaduje koncentráciu, trénuje teda pozornosť a tiež pamäť. Špecifikom knihy oproti ostatným médiám je, že rozvíja predstavivosť – imagináciu, fantáziu a kreativitu.
    No a čítanie kníh formuje aj naše vlastné myslenie. S rozsahom čítania a kvalitou prečítaných textov sa rozvíja mimoriadne dôležité kritické myslenie.</p>
  <h4 class="nadpis">6. Čítanie nás robí kultúrnejšími</h4>
  <p>Výskumy potvrdzujú, že  ľudia, ktorí čítajú, majú blízko aj k iným druhom umenia. Častejšie navštevujú rozličné kultúrne podujatia ako sú výstavy výtvarného umenia, koncerty vážnej hudby, divadelné či filmové predstavenia.
    Sme kultúrne a čitateľsky gramotnejší.  A kultúrne gramotní čitatelia sú motivovaní kultúrne dedičstvo nielen konzumovať, ale doň aj aktívne prispievať, podieľať sa na jeho tvorbe, obohacovaní a uchovávaní.</p>
  <p>Ruka v ruke spolu s kultúrnosťou ide aj správna výchova, socializácia. Práve čítanie literatúry je podľa odborníčky bohatým zdrojom výchovných stimulov. Umožňuje preberanie pozitívnych vzorov správania literárnych hrdinov, rozlišovanie dobra a zla či odmietanie negatívnych vzorov. Výchova literatúrou sa  pokladá za jednu z  najdôležitejší funkcií, resp. súčastí poslania literárnej výchovy.</p>
