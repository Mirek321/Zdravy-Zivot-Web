        <p>Nezdravé jedlo, nepravidelná strava a málo pohybu. To sú hlavné dôvody neustáleho nárastu počtu civilizačných
            ochorení akými sú vysoký krvný tlak, obezita, diabetes či rakovina. Zdravie človeka je totiž zo
            šesťdesiatich percent ovplyvňované prostredím a podiel výživy z toho tvorí približne štyridsať percent.</p>

        <p>"Zdravá výživa je dlhodobý, respektíve celoživotný štýl stravovania a preferencie potravín so zameraním na
            vysokú nutričnú kvalitu, pestrosť a plnú výživovú hodnotu,“ vraví odborník na výživu, docent Peter Minárik.
            "Jej cieľom je prevencia závažných civilizačných chorôb.“ Správna výživa je podľa neho neoddeliteľnou
            súčasťou procesu, ktorému sa hovorí životný štýl. Na niektoré choroby má až zásadný vplyv. Ide napríklad o
            nádorové ochorenie hrubého čreva či o cukrovku. Aj keď sú tieto choroby spájané s našou genetickou výbavou,
            sami môžeme ovplyvniť, do akej miery budeme svoje telo zaťažovať vonkajšími zásahmi. Aj keď môžeme mať
            genetický predpoklad ochorieť, pomocou vyváženej stravy je možné nástup ochorenia odsunúť do neskoršieho
            veku alebo výrazne ovplyvniť jeho závažnosť.</p>
            <img src="img/zdrave1.jpg" width="500" class="obrazok">
        <p>Odborníci na výživu odporúčajú pri zostavovaní jedálnička dbať nielen na typ potravín, ale tiež na spôsob ich
            spracovania. Strava by mala byť pestrá a mala by zohľadňovať zdravotný stav, vek a fyzickú námahu
            konkrétneho človeka. Dôležité je jesť pravidelne s pauzami medzi jedlami približne dve až tri hodiny.</p>

        <p>Veľmi dôležité je aj rozloženie stravy, pričom raňajky by mali tvoriť 20 % denného energetického príjmu,
            desiata 5 až 10 %, obed 35 %, olovrant 5 až 10 % a večera 25 až 30 % denného príjmu.</p>

        <p>V jedálničku by nemala chýbať vláknina, ktorá priaznivo pôsobí na fungovanie tráviaceho traktu a zloženie
            črevnej mikroflóry. Pre zdravie prospešné probiotické baktérie je rovnako dôležitá ako živná pôda pre
            rastliny. Navyše zefektívňuje obranu organizmu proti premnoženiu baktérií, ktoré by nám mohli škodiť. A
            nielen to. Znižuje vysoký cholesterol a krvný cukor. Je dôležitou súčasťou obrany proti rakovine hrubého
            čreva a optimalizáciou črevnej mikroflóry sa podieľa na lepšej imunite.</p>
            <img src="img/zdrave4.jpeg" width="500" class="obrazok">
        <p>Účinnou zbraňou sú aj funkčné potraviny. "Funkčné potraviny sú typickým predstaviteľom zdravej výživy
            človeka,“ vraví docent Minárik. Mali by sme ich jesť každý deň. Sú definované ako skupina potravín, ktoré
            okrem základnej funkcie nasýtenia a výživy organizmu majú preukázateľne zdravotne prospešné účinky. Sú
            schopné zlepšiť zdravotný stav a telesnú alebo duševnú pohodu a znížiť riziko chorôb.</p>

        <p>A ktoré potraviny, by sme, naopak, mali zo stravy vyradiť? Podľa docenta Minárika predovšetkým potraviny s
            vysokou hustotou energie vďaka veľkému obsahu tuku alebo cukru. Takéto, väčšinou nutrične „prázdne“, často
            však mimoriadne obľúbené potraviny by sme mali nahrádzať nutrične hodnotnejšími alternatívami. V každodennej
            stravovacej praxi sa to podarí dosiahnuť poklesom alebo úplným vylúčením konzumácie mastných mäsových a
            údenárskych potravín a smotanových mliečnych výrobkov. Takisto treba limitovať príjem bežných cukrárskych a
            pečivových sladkostí.</p>

        <p>Dôležitý je aj pitný režim. „Dlhodobý nedostatok tekutín môže viesť k poruchám funkcie obličiek, vzniku
            močových kameňov, zápche a bolestiam hlavy,“ upozorňuje docent Minárik. Základom je rovnováha medzi príjmom
            a výdajom tekutín.

