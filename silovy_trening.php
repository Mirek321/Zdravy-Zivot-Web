    <h4 id="scrollspyHeading1">Čo je to silový tréning?</h4>
    <p>Čo si predstavíš pod pojmom silový tréning? Možno to budú nabúchaní borci v posilke, ako už trinástu sériu drtia
        biceps.</p>

    <p> Možno to budú vzpierači a ich technicky famózne zvládnuté cviky. A možno to bude susedka, ktorá každý deň na
        dvore cvičí s vlastnou váhou.</p>

    <p> Definícií by sme v literatúre mohli nájsť mnoho. Niektoré pramene rozlišujú dokonca silový tréning od odporového
        tréningu, ktorí býva zväčša vykonávaný s menším odporom a býva súčasťou rehabilitácie.</p>

    <p> Niektoré definície percentuálne vymedzujú záťaž, s ktorou by sa mal silový tréning vykonávať.</p>
       
    <h4 class="nadpis2">Silový tréning môže byť:</h4>
    <ul>
        <li>s hmotnosťou vlastného tela (cviky s vlastnou váhou – kalistenika, street workout…)</li>
        <li>s voľnou záťažou (činka alebo iné závažie)</li>
        <li>s využitím posilňovacích strojov (napríklad legpress, rôzne kladky, Smithov stroj…)</li>


    </ul>
    <h4 class="nadpis">Aké sú výhody silového tréningu?</h4>
    <ul>
        <li>vybuduje svaly a zvýši podiel beztukovej telesnej hmoty</li>
        <li>spáli kalórie a pomôže dosiahnuť alebo udržať cieľovú hmotnosť</li>
        <li>posilní šľachy, väzy aj kĺby</li>
        <li>zvýši silu a uľahčí úlohy každodenného života</li>
        <li>zlepší kvalitu života</li>
        <li>vytvaruješ postavu podľa tvojich predstáv</li>
        <li>pomôže ti posilniť vôľu</li>
        <li>naučí ťa disciplíne</li>
        <li>získaš lepšiu kontrolu nad svojím telom</li>
        <li>naučíš sa správne pohybové vzorce (drep, mŕtvy ťah…)</li>
        <li>zvýši kostnú denzitu (hustotu) a zníži riziko vzniku osteoporózy</li>
        <li>môže pomôcť pri bolestiach chrbta, hlavy</li>
        <li>prevencia, resp. súčasť liečby civilizačných ochorení (obezita, srdcovo-cievne ochorenia)</li>
    </ul>
    <h4 class="nadpis">Ako funguje silový tréning?</h4>
    <p>Každý silový tréning spôsobuje v našich svaloch mikroskopické poškodenia. Tieto poškodenia sa v procese
        regenerácie (keď oddychuješ) opravia.</p>
    <p>To úzko súvisí aj s ďalším dôležitým bodom: aby mal sval dôvod  sa neustále „vylepšovať“, mal by byť každý tréning o niečo náročnejší.</p>
    <p>Volá sa to progresívne zaťaženie a v praxi to znamená, že pridáš buď váhu na činku, opakovania, sériu, skrátiš prestávku alebo zvýšiš frekvenciu tréningov.</p>
