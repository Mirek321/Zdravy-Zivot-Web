    <h4 class="nadpis">Cieľ silového tréningu</h4>
    <p>Hlavným cieľom silového tréningu je (aspoň u väčšiny cvičencov) hypertrofia, teda laicky povedané „rast svalov“. Bližšie sme sa jednotlivým typom hypertrofie a tomu, ako ju dosiahnuť, venovali v tomto článku.</p>

    <p>Popri hypertrofii však nemôžeme zabúdať aj na iné ciele: zlepšiť kvalitu života, zlepšiť mobilitu, schudnúť… Nech už je tvoja motivácia akákoľvek, je dosť pravdepodobné, že odpoveďou môže byť silový tréning.</p>
        <h4 class="nadpis">Prečo by si silový tréning nemal podceniť?</h4>
        <p>S vekom sa prirodzene znižuje množstvo svalovej hmoty – a to už po 30. roku! Ak zrovna patríš k tridsiatnikom (alebo si na svoju 30-ku už ledva spomínaš), o dôvod viac začať s poriadnym tréningom.</p>
        <p>To, že svaly postupne ubúdajú (asi 200 gramov za rok), dokážeš vhodným cvičením a stravou nielen pozdržať, ale dokonca zvrátiť a vybudovať novú hmotu.</p>
        <p>Ako na to?</p>
        <p>Stačí trochu správne nastaveného silového tréningu, dostatok jedla a kvalitný spánok.</p>
        <p>Ak ťa problematika budovania svalov zaujíma bližšie, nenechaj si ujsť článok „Ako nabrať svaly“, kde sme rozobrali 10 najdôležitejších bodov, ktoré potrebuješ, aby si zo svojho silového tréningu vyťažil maximum.</p>
    <h4 class="nadpis"> Zvyšuje silový tréning metabolizmus?</h4>
    <p>Množstvo energie, ktoré naše telo v kľude spáli, nazývame bazálny metabolizmus. Je pre každého jedinečný a ovplyvňuje ho veľa faktorov: od veku, množstva svalov a pohlavia až cez psychické rozpoloženie.</p>
    <p>Je všeobecne známe, že svaly vyžadujú na svoje fungovanie viac energie, než tuk. Bohužiaľ, stále nejde o závratné čísla. Je to približne 11 – 12 kcal / kg svalov / deň.</p>
    <img class="obrazok_sila" src="img/sila2.jpg">
    <h4 class="nadpis">Silový tréning a hormóny</h4>
    <p>Silový tréning je najčastejšie spájaný s mužským pohlavným hormónom – testosterónom.</p>
    <p>Zďaleka to však nie je jediný hormón, ktorý reaguje na silový tréning. Dôležitými hráčmi sú napríklad rastový hormón, IGF-1 a inzulín, ktoré takisto patria medzi silne anabolické hormóny.</p>
   
    <h4 class="nadpis">Ako si zostaviť silový tréning + princípy kvalitného tréningu</h4>
    <p>Ak chcete zosilnieť čo najviac, ako je to len možné a popri tom aj nabrať nejaké svaly, pri zostavovaní silového tréningu sa riaďte nasledovnými princípmi:</p>
    <ul>
        <li>progresívne zaťažujte svaly</li>
        <li>ideálny objem tréningu</li>
        <li>dlhšie pauzy</li>
    </ul>

    <h4 class="nadpis"> Ako na progresívne zaťažovanie</h4>
    <p>Ako sme už spomenuli, progresívne zaťažovanie je najdôležitejší princíp naberania svalov a zvyšovania sily.</p>
    <p>Problém s väčšinami tréningových plánov na mieru je ten, že iba rozpíšu nejaké cviky, navrhnú nejaké počty sérií a opakovaní. Skrátka, je to celé také nejaké. Potom to ľudia robia dokola niekoľko týždňov a na konci sa čudujú, že sa vlastne nikam poriadne nepohli. Veď ako by mohli, keď dvíhajú tie isté váhy, na ktoré si ich svaly dávno zvykli.</p>
    <p>Nemusíte zdvíhať každý krát o 10 kilo navyše, ale podstatné je, aby ste pravidelne navyšovali kilá na činke. Pri silovom tréningu zostaveného z komplexných cvikov dokážete v priemere každý týždeň navyšovať závažia o 2,5 kila.</p>
    <p>Ak po 3 mesiacoch stále dvíhate tie isté váhy, určite ste nezosilneli. Okrem toho, ak váš tréningový plán nezahŕňa progresívne zaťažovanie, v podstate máte len zhluk bezvýznamne poskladaných cvikov.</p>
    <img src="img/sila4.jpg" class="obrazok_sila" >
    <h4 class="nadpis">Rozloženie tréningu</h4>
    <p>Dnes nie je núdza o veľké množstvo rutín a tréningových plánov. Každý sa vás snaží presvedčiť, že práve jeho tréningový plán je ten najlepší.</p>
    <p>Avšak nech už máte akýkoľvek tréningový plán, pokiaľ je založený na rozumných silových princípoch, tak bude fungovať.</p>
    <p> Kvalitný tréningový plán by nemal byť jednostranne zameraný na spodnú alebo hornú časť tela ako je to pri klasickom posilňovaní na objem. </p>
    <p>Ak chcete byť celkovo silnejší a väčší, potrebujete sa zamerať na nohy aj hrudník, aj chrbát aj ruky, prípadne väčšinu z toho zapojiť počas jedného tréningu. Môžete zvoliť napríklad 2-3x do týždňa full body rutinu, ale aj:</p> 
    <ul>
        <li>tréning A: nohy, chrbát, ramená (mŕtvy ťah – čiastočne zapája aj nohy, príťahy, zhyby, pokrčenia pliec, military press…)</li>
        <li>tréning B: nohy, hrudník ruky (drepy, bench press, dipy, bicepsový zdvih veľkou činkou, sťahovanie hornej klady…)</li>
    </ul>
    
    <h4 class="nadpis">Frekvencia tréningu</h4>
    <p>Dobre navrhnutý tréningový plán je nielen založený na progresívnom zaťažovaní, ale na dostatočnej regenerácii po tréningu.</p>
    <p>Rast svalov aj zosilnenie nastáva práve mimo posilňovne. Cvičenie je len stimulom. Dobrým pravidlom je dať vaším svalom pri silovom tréningu aspoň tri dni na oddych. To znamená, že ak precvičíte nohy, tie by ste mali najbližšie cvičiť až po 72 hodinách.</p>
    <h4 class="nadpis">Dĺžka pauzy medzi sériami</h4>
    <p>Kratšie pauzy majú svoje miesto v niektorých typoch tréningov, no pri silovom je to iné. Tým, že dvíhate ťažšie váhy, budete prirodzene potrebovať aj dlhší čas na odpočinok.</p>
    <p>Pri krátkych pauzách by svaly toľko nezvládli a teda by ste museli znižovať záťaž – presný opak toho, čo chcete. Pritom štúdie preukázali, že dlhšie pauzy nemajú negatívny vplyv na rast svalov. Práve naopak. Môžete pri nich nabrať viac.</p>
    <p>Jednoduché pravidlo znie: doprajte si toľko odpočinku, koľko vám treba, aby ste zvládli ďalšiu sériu. Bežne to bývajú 2-3 minúty.</p>
