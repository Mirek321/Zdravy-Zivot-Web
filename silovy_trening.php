    <h4 class="nadpis">Silový tréning pre začiatočníkov</h4>
    <p>Silový tréning je vhodný aj pre väčšinu začiatočníkov. Jedinou výnimkou sú ľudia s chudšou a slabšou postavou s menším množstvom tuku, keďže pre nich ťažšie váhy môžu byť nadmieru zaťažujúce, najmä ak sa bavíme o komplexných cvikoch ako mŕtvy ťah a drepy. V takomto prípade by ste mali zvoliť radšej klasickú objemovku s viacerými izolovanými cvikmi, s o niečo menšími váhami a viac opakovaniami.</p>

    <p>Až keď postupne priberiete pár kíl svalov po niekoľkých mesiacoch, pokojne môžete prejsť na silový tréning, pretože vaše telo už bude vytrénované, silnejšie a znesie väčšie váhy.</p>
    <p>Ak máte akúkoľvek inú postavu, môžete pokojne zvoliť silový tréning hneď ako sa naučíte základy posilňovania a správnu techniku cvikov.</p>
