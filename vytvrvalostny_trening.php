    <p>Športové hry sú charakteristické najmä intervalovým zaťažením. To znamená, že sa strieda vysoká intenzita (šprint
      za loptou, osobné súboje) s nižšou intenzitou alebo oddychom (medzihra, krátke prestávky pri porušení
      pravidiel...). Aj v rámci jednotlivých hier absolvuje športovec intervaly rôzneho trvania a rôznej intenzity.
      Hokejista má na rozdiel od futbalistu možnosť striedať, čo mu zabezpečuje priestor na oddych. Keď už je však na
      ľade, podáva o to intenzívnejší výkon. V tenise je tiež každá „loptička“ prerušená krátkym oddychom a každé 3 hry
      je dlhšia prestávka. Športoví teoretici preto rozdelili vytrvalosť na: dlhodobú (nad 10 min.), strednodobú (8 – 10
      min.), krátkodobú (2 – 3 min.) a rýchlostnú (20 – 30 sekúnd). </p>
