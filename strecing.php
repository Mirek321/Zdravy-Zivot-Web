  <h4  class="nadpis">Prečo ho nezanedbať?</h4>
  <p>Hoci je správne rozohriatie pred tréningom nevyhnutné, mnoho ľudí ho zanedbáva, a teda sa vystavujú riziku
    poranení.

    Pokiaľ strečing vykonávate správne, udržíte si svalstvo elastickejšie a pripravené na akúkoľvek záťaž.

    Vďaka tomuto zodpovednému prístupu minimalizujete prípadné natiahnutie či poranenie úponového svalstva. Podstatnú
    úlohu zohráva pred cvičením, prináša obrovské množstvo výhod, okrem iného aj:</p>
    <ul>
   <li> zvýšenie telesnej teploty</li>
   <li> navýšenie anaeróbnej kapacity</li>
   <li> zvýšenie srdcovej frekvencie</li>
   <li> pripravenie organizmu na záťaž</li>
   <li>  zníženie rizika zranení</li>
  </ul>
