    <h4 id="scrollspyHeading1">Čo je strečing?</h4>
    <p>
      <p>Slovo strečing pochádza z anglického výrazu „to stretch“, čo znamená „preťahovať“.</p>

      <p>  Ide o metódu, pri ktorej sa svaly preťahujú a pomocou jednoduchých aj zložitejších cvikov sa stimulujú kĺby v
      tele, aby zostali pružné a pohyblivé.</p>

      <p>  Je to statická disciplína, čiže pomalým pohybom docielime konkrétnu naťahovaciu pozíciu, v ktorej určitú dobu
      zotrváme. Dá sa prirovnať napr. k joge, rovnako sleduje uvoľnenie svalov a zameranie na dýchanie.</p>

      <p>  Strečing je pomerne jednoduché cvičenie, vhodné pre každodenné použitie pre všetky vekové kategórie, prispieva k
      správnemu držaniu tela, dobrej pružnosti a celkovo k dobrému zdraviu a telesnej kondícii.</p>

      <p>  Môžu ho vykonávať aj ľudia pri bolestiach chrbta a chrbtice. V tomto prípade postačí zopár minút cvičenia denne a
      bolesti rapídne ustúpia.</p>
    </p>
    <h4  class="nadpis">Prínosy strečingu</h4>
    <p>
      <p>
      Je nutné spomenúť, čo nám strečing v konečnom dôsledku prináša a aký má vplyv na športovú výkonnosť či fyzickú
      kondíciu človeka.</p>

      <p> V skratke povedané – je poistkou proti menším i vážnejším zraneniam svalu. Strečing znižuje napätie, zlepšuje
      celkovú pohyblivosť a tým zefektívňuje športový výkon, zlepšuje krvný obeh a vylučovanie odpadových látok.</p>

      <p> Dlhodobo pomáha v boji proti starnutiu sústavy svalov a šliach.</p>

      Pojem strečing v sebe kombinuje taktiež:
      <ul>
      <li>zahriatie svalov pre neskorší výkon,</li>
      <li>cielené pretiahnutie celého tela,</li>
      <li>po tréningu uvoľnenie svalov pre lepšiu regeneráciu,</li>
      <li>ak ho vykonávate starostlivo, udržíte svalstvo pružné a odolné pri akejkoľvek záťaži.</li>
    </ul>
    </p>
    <h4 class="nadpis">Strečig pred začatím posilňovania</h4>
    <p>

      Než pristúpite ku klasickému posilňovaniu, svalstvo dôkladne zahrejte a pripravte na následnú záťaž. Krátke
      ponaťahovanie sa podľa konkrétnych zásad vám napomôže k cielenej príprave bez väčších ťažkostí.

      So strečingom pred cvičením začínajte od hlavy smerom nadol. V prípravnej fáze rozdeľuje na statický a dynamický.
      <ul>
      <li>STATICKÝ strečing – príprava svalstva s využitím pomalých a plynulých pohybov</l>
      <li>DYNAMICKÝ strečing – využitie techniky s výdržou u krajnej polohy svalstva</li>
      </ul>
    </p>
