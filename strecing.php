    <h4  class="nadpis">Statický strečing po tréningu</h4>
    <p>
     <p> Statické preťahovanie je lepšie zaradiť do cvičebného plánu až po tréningu. Pred tréningom býva spravidla
      zbytočný.</p>

      <p>Mnohé štúdie potvrdili, že nemá žiadny vplyv na zníženie rizika zranenia v priebehu záťaže. Tento fakt je o to
      paradoxnejší, že väčšinu z nás v duchu statického preťahovania pred cvičením vychovávali už od škôlky.
    </p>
      <p>
      Rozcvičenie pred tréningom by malo byť dynamické, pretože dokáže znížiť riziko zranení na minimum, zahriať telo a
      pripraviť ho na výkon.
    </p>
      Dokonca sú k dispozícii aj vedecké poznatky porovnávajúce statický strečing s dynamickým. Tie vo väčšine prípadov
      zistili, že dynamický strečing je pred tréningom podstatne efektívnejší. Niečo iné nastáva v prípade, ak sa
      snažíte o zlepšenie flexibility a na prvom mieste nie je u vás silová výkonnosť.
    </p>
    <h4 class="nadpis">Dynamický strečing</h4>
    <p>
      <img class="dyn" src="img/strecing3.jpg" align="right"> 
      <p>
      Dynamický spôsob pretiahnutia sa častejšie využíva pri pohybovo náročných športoch ako napríklad pri behu a
      začiatočníkom sa príliš neodporúča.
    </p>
    <p>
      Držte sa radšej základných zásad, ktoré sa riadia princípom preťahovania od jednoduchšieho cvičenia po
      zložitejšie. Myslite na to, že nesmie bolieť! Podľa toho spoznáte, či k nemu pristupujete správne.
    </p>
    <p>
      Pri vypätí nezadržujte dych a strážte si dýchanie. Až potom bude vaše telo a svaly pripravené k vlastnému
      posilňovaniu.
    </p>
    
    <p>
      Dynamickému strečingu venujte pred cvičením najmenej 5 minút, ideálne 10 až 15 minút. Mali by ste rozhýbať
      zápästie, lakte, ramená, chrbticu (rotácia i flexia), bedrá, kolená a aj členky. Príkladom, ktorý každý istotne
      pozná, je krúženie v zápästiach, lakťoch, ramenách a pod.
    </p>

    </p>
    
    <h4 class="nadpis">Najdôležitejšie pravidlá strečingu</h4>
    <p>Je skvelé, keď robíte strečing, ale aby ste sa vyhli možnému zraneniu, mali by ste určite dodržiavať niektoré
      základné pravidlá.</p>
    <ul>
      <li>Nikdy nenaťahujte studené svaly. To znamená, že by ste mali strečing robiť potom ako ste svaly už predtým
        ponamáhali a zahriali. Inak sa môžete ľahko zraniť, nehovoriac o neúčinnosti.</li>

      <li> Nerobte statický strečing (to znamená, keď držíte strečingové pozície po dlhší čas) pred tréningom s činkami.
        Strečing je práca pre svaly. Ak robíte ťažký strečing pred cvičením s činkami, môžete tým váš tréningový výkon
        zhoršiť. Avšak môžete, a aj vám to odporúčame, robiť dynamický strečing (je to druh pohybového strečingu, kedy
        držíte pozície len krátko).</li>

      <li> Medzi setmi taktiež nerobte žiadny statický strečing, ale venujte sa dynamickému. Nielen z toho istého
        dôvodu, ktorý sme popísali v predchádzajúcom bode, ale aj preto, že dynamický strečing pomáha sťahovaniu svalov
        (týmto spôsobom svaly, ktoré boli naťahované vopred, sa môžu počas dvíhania činiek ťažšie sťahovať).</li>

      <p>Statický strečing by ste mali robiť na konci vášho tréningu, keď sú už vaše svaly zahriate. Navyše, z
        dlhodobého
        hľadiska pomáha tvarovať vaše svaly, ktoré sa cvičením stávajú pevnejšie a to nespomíname zlepšenie pohyblivosti
        a znížené riziko zranenia.</p>
    </ul>
  
    <h4  class="nadpis">Päť najdôležitejších strečingových cvikov</h4>
    <p>Pozrime sa na päť najdôležitejších a najzákladnejších strečingových cvičení, ktoré vám na konci vášho tréningu
      pomôžu ponaťahovať všetky hlavné svalové skupiny.</p>

    <h3> 1. Hrudné svalstvo </h3>
    Položte ruky na vertikálnu tyč alebo stĺp tak, že vaše dlane smerujú dopredu. Otáčaním a krútením tela začnite
    naťahovať hrudné svaly. Udržte pozíciu na pár sekúnd, potom si oddýchnite, ale nie úplne. Opakujte naťahovanie,
    tentokrát ťažšie ako predtým. Opakujte 3-4 krát na každú stranu. Toto cvičenie vám natiahne aj bicepsy a tiež prednú
    časť svalov v ramenách.

    <h3> 2. Chrbtové svaly </h3>
    Zoberte tyč a držte ju horizontálne, pred sebou na šírku ramien. Skúste ťahať tyč tlačením bokov dozadu a dovoľte
    tak vašim chrbtovým svalom natiahnuť sa. Začnite dvíhať boky, hrbiť chrbát, zatiaľ čo ťah držíte nezmenený. Týmto
    spôsobom môžete natiahnuť chrbtové svaly a tiež trapézové svaly. Dvíhajte a sťahujte vaše boky 4-5 krát v rade.
    Tento cvik natiahne aj zadnú časť svalov v ramenách.

    <h3>3. Svaly nôh</h3>
    Držte sa niečoho pred vami a dvihnite jednu nohu. Chyťte zdvihnutú nohu vašou voľnou rukou a ťahajte ju dozadu.
    Budete cítiť ako sa svaly v nohe naťahujú.

    <h3>4. Hamstring</h3>
    Stojte so spojenými, vystretými nohami a snažte sa nahnúť čo najviac dopredu. Držte čo najviac natiahnutú pozíciu,
    potom opatrne odskočte. Opäť držte natiahnutú pozíciu, ale tentokrát trocha hlbšie ako predtým. Opakujte 4-5 krát v
    rade.

    <h3>5. Lýtka</h3>
    Stojte a oprite sa o hocijaký povrch ako by ste ho chceli odtlačiť od seba. Vystrite nohy a nechajte svaly v lýtkach
    natiahnuť sa. Striedajte pri tom ako mierne ohýbate nohy, potom ich opäť vystrite. Týmto spôsobom môžete ponaťahovať
    všetky možné časti svalov v lýtkach.
