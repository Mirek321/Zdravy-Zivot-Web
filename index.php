<p>
Niekto si môže povedať, že načo žiť zdravo. Lenže zdravý životný štýl má vplyv na lepší a zároveň aj dlhší život. A
    to výrazne.
  </p>
  <p>
    Vedci z Harvard T.H. Chan School of Public Health uskutočnili v roku 2018 masívny výskum, ako veľmi vplýva náš
    životný štýl na dĺžku života. Využili pri tom dáta z viacerých veľkých výskumov ako Nurses’ Health Study (NHS) a
    Health Professionals Follow-up Study (HPFS). V skratke, mali dáta za niekoľko desaťročí výskumov na viac ako 200
    tisíc ľuďoch, čo je obrovská vzorka a veľmi dlhý čas, aby to malo výpovednú hodnotu.
  </p>
  <p>
    Ako sa ukázalo, zdravé návyky robia obrovský rozdiel. Podľa ich analýzy, ľudia ktorí dodržiavali všetkých päť
    skúmaných kritérií, mali oveľa dlhší život: ženy sa dožívali až o 14 rokov viac a muži o 12 rokov viac. Tí, ktorí
    zdravé návyky nemali, mali oveľa väčšie riziko úmrtia na rakovinu alebo kardiovaskulárne ochorenia.
  </p>
  <p>
    Vedci v tejto štúdií zisťovali aj to, ako veľmi dokáže iba jeden faktor ovplyvniť dĺžku života. Zistili, že pridanie
    len jedného zdravého návyku zvýšilo dĺžku života až o dva roky.
  </p>
   <p>Podľa ďalších výskumov je pre optimálny efekt potrebné dodržiavať aspoň tri zdraviu prospešné návyky.</p>

    <p> Jedna štúdia publikovaná v roku 2017 zistila, že päťdesiatnici so zdravou váhou, ktorí nikdy nefajčili a pili
    alkohol s mierou žili v priemere o 7 rokov dlhšie. Iná rozsiahla analýza z roku 2012, ktorá skúmala 15
    medzinárodných štúdií na vzorke viac ako pol milióna ľudí zistila, že viac ako polovica predčasných úmrtí bola v
    dôsledku nezdravého životného štýlu, konkrétne nezdravých stravovacích návykov, obezite, nedostatku pohybu,
    nadmernému pitiu alkoholu alebo fajčenia.</p>

