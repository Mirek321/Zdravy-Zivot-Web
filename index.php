    <h4 id="scrollspyHeading1">Čo je to zdravý životný štýl?</h4>
  <section class="section1">
   <p>
    7. apríla v roku 1948 vznikla Svetová zdravotnícka organizácia (WHO) a o dva roky bol tento deň vyhlásený za Svetový
    deň zdravia. A my si ho chceme s vami aj týmto článkom pripomenúť a možno aj inšpirovať k tomu, aby ste začali žiť
    zdravšie. Skúste zadať slovné spojenie „zdravý životný štýl“ do googlu a zistíte, že za 29 sekúnd tu máte 2 940 000
    odkazov!?
  </p>
  <p>
    Nuž, tento pojem sa v poslednom desaťročí stal fenoménom, o tom niet pochýb. Mnohí o tom hovoria, píšu; iní sa tak
    snažia aj žiť. Ale viete, čo zdravý životný štýl je v skutočnosti? A ako docieliť to, aby sme ho prijali „za svoj“ a
    stal sa bežnou súčasťou aj nás a našich rodín? Veď kto by nechcel byť zdravý? Zdravie je ovplyvnené naším životným
    štýlom až z 80 percent. Tých zvyšných 20 percent pripadá na dedičnosť, zdravotnú starostlivosť, resp. ostatné
    vplyvy, ktoré tak ľahko zmeniť nevieme. Životný štýl, a najmä ten zdravý, je v našich rukách!
  </p>
  <p>
    Životný štýl je súhrn toho, ako človek žije. O zdravom životnom štýle môžeme hovoriť vtedy, keď si pravidelne
    doprajeme to, čo nám prospieva (napr. 8-hodinový spánok, pohyb na čerstvom vzduchu) a naopak – vylúčime to, čo nášmu
    organizmu škodí (fajčenie, alkohol, stres). Ale poďme sa na to pozrieť pekne postupne.
  </p>


